    private void mostrarBanner(int id) throws SQLException {
        BannerDAO bannerDAO = new BannerDAO();
        Banner banner = bannerDAO.obter(id);
        if (banner.getArquivo() != null) {
                     
            ImageIcon imageIcon = new ImageIcon(banner.getArquivo());
            JFrame jFrame = new JFrame();
            jFrame.setLayout(new FlowLayout());
            jFrame.setSize(500, 500);
            JLabel jLabel = new JLabel();
            jLabel.setIcon(imageIcon);
            jFrame.add(jLabel);
            jFrame.setVisible(true);
        } else {
            JFrame jFrame = new JFrame();
            jFrame.setLayout(new FlowLayout());
            jFrame.setSize(500, 500);
            JLabel jLabel = new JLabel("NAO DEU!!");
            jFrame.add(jLabel);
            jFrame.setVisible(true);
        }        
    }


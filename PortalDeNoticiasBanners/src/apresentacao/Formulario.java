    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Banner bannerNovo = new Banner();
            bannerNovo.setLink(this.inputLink.getText());
            bannerNovo.setLegenda(this.inputLegenda.getText());
            if (radioSuperior.isSelected()) {
                bannerNovo.setTipo("SUPERIOR");
            } else {
                bannerNovo.setTipo("INFERIOR");
            }
            new BannerDAO().adicionar(bannerNovo, this.dir);
        } catch (SQLException | FileNotFoundException ex) {
            JOptionPane.showMessageDialog(this, "erro", "erro", JOptionPane.ERROR_MESSAGE);
            Logger.getLogger(Formulario.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.listRefresh();
    }//GEN-LAST:event_jButton1ActionPerformed

